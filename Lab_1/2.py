# Program to calculate the area of a rectangle

# Take input from the user for the length of the rectangle
length = float(input("Enter the length of the rectangle: "))

# Take input from the user for the width of the rectangle
width = float(input("Enter the width of the rectangle: "))

# Calculate the area using the formula: area = length × width
area = length * width

# Display the calculated area to the user
print("The area of the rectangle is:", area)