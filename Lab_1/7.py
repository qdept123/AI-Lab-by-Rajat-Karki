# Take input from the user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Perform comparisons and display results
print("num1 == num2:", num1 == num2)   # Checks if both numbers are equal
print("num1 != num2:", num1 != num2)   # Checks if numbers are not equal
print("num1 > num2 :", num1 > num2)    # Checks if num1 is greater than num2
print("num1 < num2 :", num1 < num2)    # Checks if num1 is less than num2
print("num1 >= num2:", num1 >= num2)   # Checks if num1 is greater than or equal to num2
print("num1 <= num2:", num1 <= num2)   # Checks if num1 is less than or equal to num2